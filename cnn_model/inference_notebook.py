# =========================
# 7. FULL PIPELINE
# =========================
def inference_pipeline(image_path):

    # 1. load image
    image = load_image(image_path)

    # 2. preprocessing (split CNN & OCR)
    cnn_input = preprocess_for_cnn(image)
    ocr_input = preprocess_for_ocr(image)

    # 3. CNN prediction
    label, confidence = predict_receipt(cnn_input)

    # 4. filter non-struk
    if label == "non_struk":
        return {
            "status": "rejected",
            "stage": "cnn",
            "reason": "Bukan struk",
            "confidence": confidence
        }

    # 5. OCR (placeholder)
    text = run_ocr(ocr_input)

    # 6. parsing
    data = parse_receipt(text)

    # 7. output final
    return {
        "status": "success",
        "stage": "cnn+ocr+parser",
        "cnn_confidence": confidence,
        "data": data
    }