import cv2
import numpy as np
from PIL import Image

IMG_SIZE = (224, 224)

# =========================
# 1. LOAD IMAGE
# =========================
def load_image(image_path):
    image = cv2.imread(image_path)
    image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)
    return image


# =========================
# 2. PREPROCESS FOR CNN
# =========================
def preprocess_for_cnn(image):
    img = Image.fromarray(image)
    img = img.resize(IMG_SIZE)

    img_array = np.array(img) / 255.0
    img_array = np.expand_dims(img_array, axis=0)

    return img_array


# ============================================================
# 3. PREPROCESS FOR OCR (Bisa diubah sesuai kebutuhan OCR :D)
# ============================================================
def preprocess_for_ocr(image):
    gray = cv2.cvtColor(image, cv2.COLOR_RGB2GRAY)

    # denoise
    blur = cv2.GaussianBlur(gray, (5, 5), 0)

    # threshold (biar teks lebih jelas)
    thresh = cv2.adaptiveThreshold(
        blur,
        255,
        cv2.ADAPTIVE_THRESH_GAUSSIAN_C,
        cv2.THRESH_BINARY,
        11,
        2
    )

    return thresh


# =========================
# 4. CNN CLASSIFIER
# =========================
def predict_receipt(image_array):
    pred = model.predict(image_array, verbose=0)[0][0]

    label = "struk" if pred > 0.5 else "non_struk"
    confidence = float(pred if pred > 0.5 else 1 - pred)

    return label, confidence

